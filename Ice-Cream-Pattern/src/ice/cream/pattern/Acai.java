package ice.cream.pattern;

/**
 *
 * @author Uillian
 */
public class Acai {

    private String nome = "";
    private String status = "";
    private boolean banana = false;
    private boolean morango = false ;
    private boolean nutela = false ;
    private boolean leiteCondensado = false ;
    private boolean biz = false ;
    private boolean sonhoDeValsa = false ;
    private boolean confete = false ;
    private boolean granola = false ;
    private boolean ninho = false ;

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public boolean isBanana() {
        return banana;
    }

    public void setBanana(boolean banana) {
        this.banana = banana;
    }

    public boolean isMorango() {
        return morango;
    }

    public void setMorango(boolean morango) {
        this.morango = morango;
    }

    public boolean isNutela() {
        return nutela;
    }

    public void setNutela(boolean nutela) {
        this.nutela = nutela;
    }

    public boolean isLeiteCondensado() {
        return leiteCondensado;
    }

    public void setLeiteCondensado(boolean leiteCondensado) {
        this.leiteCondensado = leiteCondensado;
    }

    public boolean isBiz() {
        return biz;
    }

    public void setBiz(boolean biz) {
        this.biz = biz;
    }

    public boolean isSonhoDeValsa() {
        return sonhoDeValsa;
    }

    public void setSonhoDeValsa(boolean sonhoDeValsa) {
        this.sonhoDeValsa = sonhoDeValsa;
    }

    public boolean isConfete() {
        return confete;
    }

    public void setConfete(boolean confete) {
        this.confete = confete;
    }

    public boolean isGranola() {
        return granola;
    }

    public void setGranola(boolean granola) {
        this.granola = granola;
    }

    public boolean isNinho() {
        return ninho;
    }

    public void setNinho(boolean ninho) {
        this.ninho = ninho;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }
    
}
